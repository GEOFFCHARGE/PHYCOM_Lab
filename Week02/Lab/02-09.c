// Lab 02-09 Character Shifting
 
#include <stdio.h>
   
int main()
{
    char one;
    char two;
    char three;
    char four;
    char five;
    scanf(" %c", &one);
    scanf(" %c", &two);
    scanf(" %c", &three);
    scanf(" %c", &four);
    scanf(" %c", &five);
    printf("%c\n", one + 1);
    printf("%c\n", two);
    printf("%c\n", three + 1);
    printf("%c\n", four);
    printf("%c", five + 1);
    return 0;
}
